    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        int row = vp.getjTableReservas().getSelectedRow();
        String fecha=String.valueOf(vp.getModel().getValueAt(row,1));
        String salon = String.valueOf(vp.getModel().getValueAt(row,4));
        for (int i = 0; i < vp.getHotel().getEventos().size(); i++) {
            if (vp.getHotel().getEventos().get(i).getId()==vp.getHotel().getReservas().get(row).getIdEvento()) {
                vp.getHotel().getEventos().remove(i);
            }
        }
        vp.getHotel().getReservas().remove(row);
        vp.getManejoDB().eliminarEvento(Integer.parseInt(String.valueOf(vp.getModel().getValueAt(row, 0))));
        vp.proximasReservas(vp.getHotel(), vp.getjComboBoxSalon().getSelectedIndex()+1);
        vp.porcentajeReservas();
        vp.getModel().refreshTableModel(vp.getHotel());
        vp.getjTextAreaInformacion().setText("");
        dispose();
    }//GEN-LAST:event_jButtonEliminarActionPerformed

